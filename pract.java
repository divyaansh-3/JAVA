public class pract {
    public static void main(String[] args) {
        char[] arr = {'d','i','v','y','a','a','n','s','h'};
        
        for (int i = 0; i < arr.length; i++) {
            System.out.print(arr[i]);
        }
        System.out.println("\n");
        for(int j=arr.length-1;j>=0;j--){

            System.out.print(arr[j]);
        }
        
    }

}
